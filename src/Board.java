    public Board(String size) {
        // Initialize the board based on the size
        switch (size) {
            case "Small":
                width = SMALL_MAP[0].length;
                height = SMALL_MAP.length;
                initializeBoard(SMALL_MAP);
                break;
            case "Medium":
                width = MEDIUM_MAP[0].length;
                height = MEDIUM_MAP.length;
                initializeBoard(MEDIUM_MAP);
                break;
            case "Large":
                width = LARGE_MAP[0].length;
                height = LARGE_MAP.length;
                initializeBoard(LARGE_MAP);
                break;
            case "Extra Large":
                width = EXTRA_LARGE_MAP[0].length;
                height = EXTRA_LARGE_MAP.length;
                initializeBoard(EXTRA_LARGE_MAP);
                break;
            case "Giant":
                width = GIANT_MAP[0].length;
                height = GIANT_MAP.length;
                initializeBoard(GIANT_MAP);
                break;
            default:
                width = SMALL_MAP[0].length;
                height = SMALL_MAP.length;
                initializeBoard(SMALL_MAP);
                break;
        }

        upgrades = new ArrayList<>();
    }

    private void initializeBoard(int[][] map) {
        walls = new boolean[height][width];
        pellets = new boolean[height][width];

        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                if (map[y][x] == 1) {
                    walls[y][x] = true;
                    pellets[y][x] = false;
                } else {
                    walls[y][x] = false;
                    pellets[y][x] = true;
                }
            }
        }
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public boolean isWall(int x, int y) {
        return walls[y][x];
    }

    public boolean hasPellet(int x, int y) {
        return pellets[y][x];
    }

    public void removePellet(int x, int y) {
        pellets[y][x] = false;
    }

    public List<Upgrade> getUpgrades() {
        return upgrades;
    }

    public void addUpgrade(Upgrade upgrade) {
        upgrades.add(upgrade);
    }

    public void removeUpgrade(Upgrade upgrade) {
        upgrades.remove(upgrade);
    }
