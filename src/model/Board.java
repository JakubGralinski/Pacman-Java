    public Board(String size) {
        // Initialize the board based on the size
        switch (size) {
            case "Map1":
                width = MAP1[0].length;
                height = MAP1.length;
                initializeBoard(MAP1);
                break;
            case "Map2":
                width = MAP2[0].length;
                height = MAP2.length;
                initializeBoard(MAP2);
                break;
            case "Map3":
                width = MAP3[0].length;
                height = MAP3.length;
                initializeBoard(MAP3);
                break;
            case "Map4":
                width = MAP4[0].length;
                height = MAP4.length;
                initializeBoard(MAP4);
                break;
            case "Map5":
                width = MAP5[0].length;
                height = MAP5.length;
                initializeBoard(MAP5);
                break;
            default:
                width = MAP1[0].length;
                height = MAP1.length;
                initializeBoard(MAP1);
                break;
        }

        upgrades = new ArrayList<>();
    }
    private void initializeBoard(int[][] map) {
        walls = new boolean[height][width];
        pellets = new boolean[height][width];

        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                if (map[y][x] == 1) {
                    walls[y][x] = true;
                    pellets[y][x] = false;
                } else {
                    walls[y][x] = false;
                    pellets[y][x] = true;
                }
            }
        }
    }

    public List<int[]> getValidSpawnPositions() {
        List<int[]> positions = new ArrayList<>();
        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                if (!walls[y][x]) {
                    positions.add(new int[]{x, y});
                }
            }
        }
        return positions;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public boolean isWall(int x, int y) {
        return walls[y][x];
    }

    public boolean hasPellet(int x, int y) {
        return pellets[y][x];
    }

    public void removePellet(int x, int y) {
        pellets[y][x] = false;
    }

    public List<Upgrade> getUpgrades() {
        return upgrades;
    }

    public void addUpgrade(Upgrade upgrade) {
        upgrades.add(upgrade);
    }

    public void removeUpgrade(Upgrade upgrade) {
        upgrades.remove(upgrade);
    }

    public boolean hasPellets() {
        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                if (pellets[y][x]) {
                    return true;
                }
            }
        }
        return false;
    }

